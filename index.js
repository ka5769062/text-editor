
function mybold(){

    var thicker =  document.getElementById('mytext')

if(thicker.style.fontWeight == "bold"){

    thicker.style.fontWeight = "normal";


}

else{

    thicker.style.fontWeight = "bold";

}


}


function myitalic(){


    var thicker =  document.getElementById('mytext')


 if(thicker.style.fontStyle == "italic"){

    thicker.style.fontStyle = "normal"

 }

else{

    thicker.style.fontStyle = "italic";

}

 }


